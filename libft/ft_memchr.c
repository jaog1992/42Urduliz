#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t		i;

	i = 0;
	if (c != 0 && n != 0)
	{
		while (i <= (n - 1))
		{
			if (*(char *)(s + i) == (char)c)
				return ((char *)(s + i));
			i++;
		}
	}
	return (NULL);
}
